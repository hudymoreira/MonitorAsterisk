<p>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Relação de ligações efetuadas pelos os usuários do Asterisk. Aqui é possível associar um ramal a um usuário e filtrar as ligações por ramal, mês e ano.
<br />

&nbsp;É possível também:
<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Exportar o resultado da pesquisa para o Excel;<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Escutar as ligações.
<br />

&nbsp;Os dados disponives são:
<br /><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ramal;<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Data e hora da ligação;<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Áudio da ligação (ou nome do arquivo);<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Numero do telefone que foi efetuado a ligação;<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Duração da ligação.<br />


</p>
